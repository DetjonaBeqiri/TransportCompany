    public void loadKompaniaComboBo() throws KompaniaException{
        try {
          List<Kompaniatbl> kompanite = kR.findAll();
//          kcbm.add(kompanite);
//          jComboBox1.setModel(kcbm);
            for (int i = 0; i < kompanite.size(); i++) {
                jComboBox1.addItem(kompanite.get(i).getIDKompanise()+" "+kompanite.get(i).getEmriKompanise());
            }
        } catch (Exception e) {
        }
        
    }
    public void loadAutobusetTable() throws KompaniaException{
        try{
            List<Autobusettbl> autobuset = aR.findAll();
            atm.addList(autobuset);
            AutobusetTable.setModel(atm);
         
        }catch(AutobusiException cE){
            Logger.getLogger(AutobusiException.class.getName()).log(Level.SEVERE,null,cE);
        }
    }
    private void tabelaSelectedIndexChangeAutobuset(){
        final ListSelectionModel rowSM = AutobusetTable.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener(){
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(e.getValueIsAdjusting()){
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if(selectedIndex > -1){
                    Autobusettbl c = atm.getAutobusettbl(selectedIndex);
                    emriField.setText(c.getEmriAutobusit());
                    shoferiField.setText(c.getShoferi());
                    kondukteriField.setText(c.getKondukteri());
                    IDAutobusitField.setText(c.getIDAutobusi().toString());
                    
                } //To change body of generated methods, choose Tools | Templates.
            }
        });
    }
