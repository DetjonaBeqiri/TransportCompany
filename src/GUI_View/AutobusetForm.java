    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
          int row = AutobusetTable.getSelectedRow();
       if(row == -1){
           JOptionPane.showMessageDialog(null, "Ju lutem zgjedheni autobusin per te perditesuar!");
       }
       else{
          
            Autobusettbl autobuset = new Autobusettbl();
      Kompaniatbl k = new Kompaniatbl();
      k.setIDKompanise(Integer.parseInt(IDkompanis.getText()));
      autobuset.setEmriAutobusit(emriField.getText());
      autobuset.setShoferi(shoferiField.getText());
      autobuset.setKondukteri(kondukteriField.getText());
      autobuset.setIDKompanise(k);
        try {
            aR.edit(autobuset);
            JOptionPane.showMessageDialog(null, "Autobusi i shtua me sukses!");
        } catch (Exception e) {
        }
       }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = AutobusetTable.getSelectedRow();
       if(row == -1){
           JOptionPane.showMessageDialog(null, "Please choose a costumer to delete!");
       }
       else{
          
           try {
               Autobusettbl c = atm.getAutobusettbl(row);
               aR.delete(c);
          
               JOptionPane.showMessageDialog(null, "Autobusi eshte fshire me sukses.");
           } catch (AutobusiException ex) {
               Logger.getLogger(KompaniaException.class.getName()).log(Level.SEVERE, null, ex);
               JOptionPane.showMessageDialog(null, ex);
           }
       }
    }//GEN-LAST:event_jButton3ActionPerformed

